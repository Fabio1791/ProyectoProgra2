   /**
    * Método que se ejecuta al hacer clic en el botón "Agregar".
    * Realiza la validación de los datos ingresados y agrega un producto al inventario.
    * @param evt El evento de acción que desencadena la ejecución del método.
    */
    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
          /**
        * Realiza la validación de los datos ingresados y agrega un producto al inventario si los datos son válidos.
        * Muestra mensajes de advertencia o éxito según corresponda.
        */
      if(validar()){
            if(!BI_Producto.verificarExistencia(nombre)){
                // Generar un nuevo código para el producto
                if(codigo == 0){
                    // Crear un nuevo producto con los datos ingresados
                    int codigoAux = BI_Producto.ultimoCodigo() + 1;
                    // Agregar el producto al inventario
                    BI_Producto.agregar(new Product(codigoAux, nombre, precio, existencia));
                    // Actualizar la tabla en la interfaz de usuario con la lista actualizada de producto
                    this.tblTabla.setModel(new Tabla_Inventario(BI_Producto.getLista()));
                    // Limpiar los datos ingresados en la interfaz de usuario
                    limpiarDatos();
                     // Mostrar un mensaje de éxito indicando que el producto ha sido agregado
                     JOptionPane.showMessageDialog(this, "Producto agregado","AGREGADO", 1);
                }else{
                    // Mostrar un mensaje de advertencia indicando que se ha ingresado un código de línea
                    JOptionPane.showMessageDialog(this, "ADVERTENCIA DE LINEA CODIGO","ADVERTENCIA",2);
                }
            }else{
                // Mostrar un mensaje de advertencia indicando que el producto ya existe en el inventario
                JOptionPane.showMessageDialog(this, "ADVERTENCIA DE LINEA VERIFICAR EXISTENCIA","ADVERTENCIA",2); 
            }
        }else{
            // Mostrar un mensaje de advertencia indicando que los datos ingresados no son válidos
            JOptionPane.showMessageDialog(this, "ADVERTENCIA DE LINEA VALIDAR","ADVERTENCIA",2);
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

