/**
        * Valida los datos ingresados en los campos de texto de la interfaz de usuario.
        * @return true si los datos son válidos, false si hay algún error.
        */
        public boolean validar(){
            try{
                // Validar el código
                codigo = Integer.parseInt("".equals(txtCodigo.getText())? "0" 
                        : txtCodigo.getText());
                // Validar el nombre
                nombre = txtNombre.getText();
                // Validar el precio
                precio = Double.parseDouble(txtPrecio.getText());
                // Validar la existencia
                existencia = Integer.parseInt(txtExistencia.getText()); 
                // Todos los datos son válidos
                return true;
            }
            catch(Exception e){
                // Hubo un error al convertir los datos o algún campo está vacío
                return false;
            }
        }
        /**
        * Limpia los campos de texto en la interfaz de usuario.
        */
        public void limpiarDatos(){
            this.txtCodigo.setText("");
            this.txtNombre.setText("");
            this.txtPrecio.setText("");
            this.txtExistencia.setText("");
        }


